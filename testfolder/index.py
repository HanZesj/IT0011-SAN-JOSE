my_string = "Hello, World!"

# Accessing the first character
print(my_string[0])  # Output: H

# Accessing a substring from index 2 to 4 (5 is not included)
print(my_string[2:5])  # Output: llo

# Accessing the last character
print(my_string[-1])  # Output: !

# Accessing a substring from the beginning to index 4
print(my_string[:5])  # Output: Hello

# Accessing a substring from index 7 to the end
print(my_string[7:])  # Output: World!

# Accessing the entire string
print(my_string[:])  # Output: Hello, World!

# Accessing a substring with a step
print(my_string[::2])  # Output: Hlo ol!
